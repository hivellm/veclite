#!/usr/bin/env python3
"""Reproducible embedded-vector-store benchmark (SPEC-015 TST-042; DAG T6.4).

Compares VecLite against peer embedded stores — sqlite-vec, LanceDB, Chroma —
and, optionally, the Vectorizer server, on one fixed workload:

  * a deterministic dataset (fixed-seed splitmix64 -> unit-normalized f32,
    no `rand`/`numpy.random` state to drift),
  * build the index, then run K nearest-neighbour queries at top-`k`,
  * measure build time, query p50/p95 latency, and recall@k against an exact
    brute-force reference.

Each store is an *adapter*; a store whose package is not installed is **skipped
and reported as such** — never silently dropped, never fabricated. Run what you
have; the numbers that print are the numbers that ran. Results are written to
`bench/results/<timestamp>.json` and printed as a markdown table for the report.

Usage:
    python bench/harness.py [--vectors N] [--queries Q] [--dim D] [--k K]
                            [--metric cosine|l2] [--stores a,b,c]
                            [--vectorizer-url URL] [--out results.json]

Dataset: a **clustered** mixture of Gaussians (representative of real embeddings,
which live near topic centroids — not uniform random, the known ANN worst case).
Generated by a seeded NumPy RNG, so the dataset is a pure function of
(--vectors, --queries, --dim, --metric, --clusters, seed); rerunning with the
same flags on equivalent hardware reproduces the recall exactly and the
latencies within the machine's run-to-run variance. Disclose the hardware
profile (CPU, RAM, OS) alongside any published number.
"""

from __future__ import annotations

import argparse
import json
import os
import platform
import statistics
import sys
import tempfile
import time
from dataclasses import dataclass, field, asdict

import numpy as np

SEED = 0x5EC11_7E5


# ── Deterministic clustered dataset (seeded NumPy) ───────────────────────────

@dataclass
class Dataset:
    dim: int
    metric: str
    ids: list[str]
    vectors: np.ndarray  # (n, dim) float32
    queries: np.ndarray  # (q, dim) float32
    # Exact top-k ids per query (brute-force reference), filled by exact_topk.
    exact: list[list[str]] = field(default_factory=list)


def build_dataset(n: int, q: int, dim: int, metric: str, clusters: int) -> Dataset:
    """A mixture of `clusters` Gaussians — vectors cluster near random centroids,
    the way real embeddings cluster near topics. For cosine, everything is
    L2-normalized (unit vectors) so the metric is well-defined."""
    rng = np.random.default_rng(SEED)
    centroids = rng.standard_normal((clusters, dim)).astype(np.float32)
    assign = rng.integers(0, clusters, size=n)
    vectors = centroids[assign] + 0.35 * rng.standard_normal((n, dim)).astype(np.float32)
    # Queries near random centroids too (a realistic "find similar" workload).
    q_assign = rng.integers(0, clusters, size=q)
    queries = centroids[q_assign] + 0.35 * rng.standard_normal((q, dim)).astype(np.float32)

    vectors = vectors.astype(np.float32)
    queries = queries.astype(np.float32)
    if metric == "cosine":
        vectors /= np.linalg.norm(vectors, axis=1, keepdims=True).clip(1e-9)
        queries /= np.linalg.norm(queries, axis=1, keepdims=True).clip(1e-9)
    ids = [f"v{i:07d}" for i in range(n)]
    return Dataset(dim=dim, metric=metric, ids=ids, vectors=vectors, queries=queries)


def exact_topk(ds: Dataset, k: int) -> None:
    """Fill ds.exact with the brute-force nearest ids per query (recall ref),
    via a single vectorized pass."""
    v = ds.vectors
    ids = np.array(ds.ids)
    ds.exact = []
    for query in ds.queries:
        if ds.metric == "l2":
            d = np.sum((v - query) ** 2, axis=1)
        else:  # cosine distance on unit vectors == 1 - dot
            d = 1.0 - v @ query
        nearest = np.argpartition(d, k)[:k]
        nearest = nearest[np.argsort(d[nearest])]
        ds.exact.append([str(x) for x in ids[nearest]])


# ── Store adapters ───────────────────────────────────────────────────────────

class Adapter:
    name = "base"

    def available(self) -> tuple[bool, str]:
        """(installed?, reason-if-not)."""
        return False, "not implemented"

    def build(self, ds: Dataset) -> None: ...
    def query(self, vec: list[float], k: int) -> list[str]: ...
    def close(self) -> None: ...


class VecLiteAdapter(Adapter):
    name = "veclite"

    def available(self):
        try:
            import veclite  # noqa: F401
            return True, ""
        except Exception as e:  # pragma: no cover - env dependent
            return False, f"import veclite: {e}"

    def build(self, ds):
        import veclite
        self._dir = tempfile.mkdtemp()
        self.db = veclite.Database.open(os.path.join(self._dir, "bench.veclite"))
        metric = "cosine" if ds.metric == "cosine" else "euclidean"
        # Full precision so recall reflects the index, not quantization. Vectors
        # pass as NumPy rows (the binding's zero-copy fast path).
        self.coll = self.db.create_collection(
            "bench", ds.dim, metric=metric, quantization_bits=0
        )
        self.coll.upsert_batch(ds.ids, list(ds.vectors))

    def query(self, vec, k):
        return [h["id"] for h in self.coll.search(vec, limit=k)]

    def close(self):
        try:
            import shutil
            shutil.rmtree(self._dir, ignore_errors=True)
        except Exception:
            pass


class SqliteVecAdapter(Adapter):
    name = "sqlite-vec"

    def available(self):
        try:
            import sqlite_vec  # noqa: F401
            import sqlite3  # noqa: F401
            return True, ""
        except Exception as e:
            return False, f"import sqlite_vec: {e}"

    def build(self, ds):
        import sqlite3
        import sqlite_vec
        self.metric = ds.metric
        self.db = sqlite3.connect(":memory:")
        self.db.enable_load_extension(True)
        sqlite_vec.load(self.db)
        self.db.enable_load_extension(False)
        # sqlite-vec exposes L2 and cosine via the distance metric on the vec0
        # virtual table.
        dist = "cosine" if ds.metric == "cosine" else "L2"
        self.db.execute(
            f"CREATE VIRTUAL TABLE bench USING vec0(embedding float[{ds.dim}] distance_metric={dist})"
        )
        rows = [
            (i, ds.vectors[i].tobytes())
            for i in range(len(ds.vectors))
        ]
        self.db.executemany("INSERT INTO bench(rowid, embedding) VALUES (?, ?)", rows)
        self.db.commit()
        self.ids = ds.ids

    def query(self, vec, k):
        blob = np.asarray(vec, dtype=np.float32).tobytes()
        cur = self.db.execute(
            "SELECT rowid FROM bench WHERE embedding MATCH ? ORDER BY distance LIMIT ?",
            (blob, k),
        )
        return [self.ids[row[0]] for row in cur.fetchall()]

    def close(self):
        try:
            self.db.close()
        except Exception:
            pass


class LanceDbAdapter(Adapter):
    name = "lancedb"

    def available(self):
        try:
            import lancedb  # noqa: F401
            return True, ""
        except Exception as e:
            return False, f"import lancedb: {e}"

    def build(self, ds):
        import lancedb
        self._dir = tempfile.mkdtemp()
        self.metric = "cosine" if ds.metric == "cosine" else "l2"
        db = lancedb.connect(self._dir)
        data = [
            {"id": ds.ids[i], "vector": ds.vectors[i].tolist()}
            for i in range(len(ds.ids))
        ]
        self.table = db.create_table("bench", data=data)

    def query(self, vec, k):
        rows = self.table.search(vec).metric(self.metric).limit(k).to_list()
        return [r["id"] for r in rows]

    def close(self):
        import shutil
        shutil.rmtree(getattr(self, "_dir", ""), ignore_errors=True)


class ChromaAdapter(Adapter):
    name = "chroma"

    def available(self):
        try:
            import chromadb  # noqa: F401
            return True, ""
        except Exception as e:
            return False, f"import chromadb: {e}"

    def build(self, ds):
        import chromadb
        space = "cosine" if ds.metric == "cosine" else "l2"
        self.client = chromadb.EphemeralClient()
        self.coll = self.client.create_collection(
            "bench", metadata={"hnsw:space": space}
        )
        # Chroma caps a single add() at ~5461 rows; chunk to respect it.
        embeddings = ds.vectors.tolist()
        step = 5000
        for start in range(0, len(ds.ids), step):
            end = start + step
            self.coll.add(ids=ds.ids[start:end], embeddings=embeddings[start:end])

    def query(self, vec, k):
        res = self.coll.query(query_embeddings=[np.asarray(vec, dtype=np.float32).tolist()], n_results=k)
        return list(res["ids"][0])

    def close(self):
        pass


class VectorizerAdapter(Adapter):
    """The Vectorizer server over HTTP — gated on --vectorizer-url."""

    name = "vectorizer-server"

    def __init__(self, url: str | None, password: str | None) -> None:
        self.url = url
        self.password = password

    def available(self):
        if not self.url:
            return False, "no --vectorizer-url"
        try:
            import urllib.request  # noqa: F401
            return True, ""
        except Exception as e:  # pragma: no cover
            return False, str(e)

    def build(self, ds):  # pragma: no cover - needs a running server
        raise NotImplementedError(
            "the server adapter is a stub: wire it to your Vectorizer instance's "
            "/insert_vectors + /search endpoints (see crates/veclite/tests/parity.rs "
            "for the exact BYO-vector calls the parity gate already uses)"
        )


# ── Runner ───────────────────────────────────────────────────────────────────

@dataclass
class StoreResult:
    store: str
    build_s: float
    query_p50_ms: float
    query_p95_ms: float
    recall_at_k: float
    queries_per_s: float


def recall(expected: list[str], got: list[str]) -> float:
    if not expected:
        return 1.0
    hit = len(set(expected) & set(got))
    return hit / len(expected)


def run_store(adapter: Adapter, ds: Dataset, k: int) -> StoreResult:
    t0 = time.perf_counter()
    adapter.build(ds)
    build_s = time.perf_counter() - t0

    latencies_ms: list[float] = []
    recalls: list[float] = []
    for i, query in enumerate(ds.queries):
        q0 = time.perf_counter()
        got = adapter.query(query, k)
        latencies_ms.append((time.perf_counter() - q0) * 1000.0)
        recalls.append(recall(ds.exact[i], got))
    adapter.close()

    latencies_ms.sort()
    p50 = latencies_ms[len(latencies_ms) // 2]
    p95 = latencies_ms[min(len(latencies_ms) - 1, int(len(latencies_ms) * 0.95))]
    mean_ms = statistics.fmean(latencies_ms)
    return StoreResult(
        store=adapter.name,
        build_s=round(build_s, 4),
        query_p50_ms=round(p50, 4),
        query_p95_ms=round(p95, 4),
        recall_at_k=round(statistics.fmean(recalls), 4),
        queries_per_s=round(1000.0 / mean_ms, 1) if mean_ms else 0.0,
    )


def hardware_profile() -> dict:
    return {
        "cpu": platform.processor() or platform.machine(),
        "python": platform.python_version(),
        "os": f"{platform.system()} {platform.release()}",
        "machine": platform.machine(),
    }


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--vectors", type=int, default=10_000)
    ap.add_argument("--queries", type=int, default=200)
    ap.add_argument("--dim", type=int, default=256)
    ap.add_argument("--k", type=int, default=10)
    ap.add_argument("--metric", choices=["cosine", "l2"], default="cosine")
    ap.add_argument("--clusters", type=int, default=64)
    ap.add_argument("--stores", default="veclite,sqlite-vec,lancedb,chroma")
    ap.add_argument("--vectorizer-url", default=os.environ.get("VECLITE_PARITY_URL"))
    ap.add_argument("--out", default=None)
    args = ap.parse_args()

    print(
        f"[bench] dataset: {args.vectors} x {args.dim} {args.metric}, "
        f"{args.clusters} clusters, {args.queries} queries, top-{args.k}",
        file=sys.stderr,
    )
    ds = build_dataset(args.vectors, args.queries, args.dim, args.metric, args.clusters)
    exact_topk(ds, args.k)

    catalog = {
        "veclite": VecLiteAdapter(),
        "sqlite-vec": SqliteVecAdapter(),
        "lancedb": LanceDbAdapter(),
        "chroma": ChromaAdapter(),
        "vectorizer-server": VectorizerAdapter(
            args.vectorizer_url, os.environ.get("VECLITE_PARITY_PASSWORD")
        ),
    }
    wanted = [s.strip() for s in args.stores.split(",") if s.strip()]

    results: list[StoreResult] = []
    skipped: list[tuple[str, str]] = []
    for name in wanted:
        adapter = catalog.get(name)
        if adapter is None:
            skipped.append((name, "unknown store"))
            continue
        ok, reason = adapter.available()
        if not ok:
            print(f"[bench] {name:>18} SKIP — {reason}", file=sys.stderr)
            skipped.append((name, reason))
            continue
        print(f"[bench] {name:>18} running…", file=sys.stderr)
        try:
            results.append(run_store(adapter, ds, args.k))
        except Exception as e:  # a store that errors is reported, not fatal
            print(f"[bench] {name:>18} ERROR — {e}", file=sys.stderr)
            skipped.append((name, f"error: {e}"))

    report = {
        "config": {
            "vectors": args.vectors,
            "queries": args.queries,
            "dim": args.dim,
            "k": args.k,
            "metric": args.metric,
            "clusters": args.clusters,
        },
        "hardware": hardware_profile(),
        "results": [asdict(r) for r in results],
        "skipped": [{"store": s, "reason": r} for s, r in skipped],
    }

    # Markdown table to stdout (drop straight into the report).
    print()
    print("| store | build (s) | query p50 (ms) | query p95 (ms) | recall@k | queries/s |")
    print("|---|---|---|---|---|---|")
    for r in results:
        print(
            f"| {r.store} | {r.build_s} | {r.query_p50_ms} | {r.query_p95_ms} "
            f"| {r.recall_at_k} | {r.queries_per_s} |"
        )
    for s, reason in skipped:
        print(f"| {s} | — | — | — | — | not run: {reason} |")

    out = args.out
    if out is None:
        os.makedirs("bench/results", exist_ok=True)
        out = os.path.join("bench", "results", f"bench-{int(time.time())}.json")
    with open(out, "w", encoding="utf-8") as fh:
        json.dump(report, fh, indent=2)
    print(f"\n[bench] wrote {out}", file=sys.stderr)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
